<template>
  <div class="progress-circle">
    <svg :width="radius" :height="radius" viewBox="0 0 100 100" version="1.1"
         xmlns="http://www.w3.org/2000/svg">
      <!--内圈-->
      <circle class="progress-background"
              r="50"
              cx="50"
              cy="50"
              fill="transparent"></circle>
      <!--外圈-->
      <circle class="progress-bar"
              r="50"
              cx="50"
              cy="50"
              fill="transparent"
              :stroke-dasharray="dashArray" :stroke-dashoffset="dashOffset"></circle>
    </svg>
    <slot/>
  </div>
</template>

<script type="text/ecmascript-6">
  export default {
    props: {
      // 半径
      radius: {
        type: Number,
        default: 100
      },
      // 百分比(歌曲播放时间/总时长)
      percent: {
        type: Number,
        default: 0
      }
    },
    data() {
      return {
        // 周长 = π * 直径
        dashArray: Math.PI * 100
      }
    },
    computed: {
      // 运动轨迹（1为无轨迹，0为满圈）
      dashOffset() {
        return (1 - this.percent) * this.dashArray
      }
    }
  }
</script>

