<template>
  <ul class="switches">
    <li
      class="switch-item"
      v-for="(item, index) in switches"
      :key="index"
      @click="selectItem(index)"
      :class="{ active: currentIndex === index }"
    >
      <span>{{ item.name }}</span>
    </li>
  </ul>
</template>

<script>
export default {
  props: {
    // 窗口文本数组
    switches: {
      type: Array,
      default: () => []
    },

    // 当前索引（高亮）
    currentIndex: {
      type: Number,
      default: 0
    }
  },
  methods: {
    // 选中开关
    selectItem(index) {
      this.$emit('switch', index)
    }
  }
}
</script>

