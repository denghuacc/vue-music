<template>
  <div class="music-list">
    <div
      class="back"
      @click="back"
    >
      <i class="icon-back"></i>
    </div>
    <h1
      class="title"
      v-html="title"
    ></h1>
    <div
      class="bg-image"
      :style="bgStyle"
      ref="bgImage"
    >
      <div class="play-wrapper">
        <div
          class="play"
          v-show="songs.length > 0"
          ref="playBtn"
          @click="random"
        >
          <i class="icon-play"></i>
          <span class="text">随机播放全部</span>
        </div>
      </div>
      <div
        class="filter"
        ref="filter"
      ></div>
    </div>
    <div
      class="bg-layer"
      ref="layer"
    ></div>
    <Scroll
      @scroll="scroll"
      :probe-type="probeType"
      :listen-scroll="listenScroll"
      :data="songs"
      class="list"
      ref="list"
    >
      <div class="song-list-wrapper">
        <SongList
          :rank="rank"
          @select="selectDisc"
          :songs="songs"
        />
      </div>
      <div
        class="loading-container"
        v-show="!songs.length"
      >
        <Loading />
      </div>
    </Scroll>
  </div>
</template>

<script lang="ts">
import { Vue, Component, Prop, Watch } from 'vue-property-decorator'
import Scroll from '@/components/Scroll.vue'
import SongList from '@/components/SongList.vue'
import Loading from '@/components/Loading.vue'
import { prefixStyle } from '@/assets/utils/dom'
import { PlaylistMixin } from '@/assets/utils/mixin'
import { Action } from 'vuex-class'

const RESERVED_HEIGHT: number = 40
const transform = prefixStyle('transform')
const backdrop = prefixStyle('backdrop-filter')

@Component({
  components: {
    Scroll,
    SongList,
    Loading
  }
})
export default class MusicList extends PlaylistMixin {
  @Action
  public selectPlay: any
  @Action
  public randomPlay: any

  @Prop({ default: '' })
  public bgImage!: string
  @Prop({ default: [] })
  public songs!: any[]
  @Prop({ default: '' })
  public title!: string
  @Prop({ default: false })
  public rank!: boolean

  public scrollY: number = 0
  public probeType: number = 3
  public listenScroll: boolean = true
  public imageHeigth: number = 0
  public minTranslateY: number = 0
  public $refs: any

  public mounted() {
    this.imageHeigth = this.$refs.bgImage.clientHeight
    this.minTranslateY = -this.imageHeigth + RESERVED_HEIGHT
    this.$refs.list.$el.style.top = `${this.imageHeigth}px`
  }

  get bgStyle() {
    return `background-image:url(${this.bgImage})`
  }

  public handlePlaylist(playlist: any) {
    const bottom = playlist.length > 0 ? '60px' : ''
    this.$refs.list.$el.style.bottom = bottom
    this.$refs.list.refresh()
  }

  public scroll(pos: any) {
    this.scrollY = pos.y
  }

  public back() {
    this.$router.back()
  }

  public selectDisc(disc: any, index: number) {
    this.selectPlay({
      list: this.songs,
      index
    })
  }

  public random() {
    this.randomPlay({
      list: this.songs
    })
  }

  @Watch('scrollY')
  private onScrollYChange(newY: number) {
    const translateY = Math.max(this.minTranslateY, newY)
    let zIndex = 0
    let scale = 1
    let blur = 0
    const percent = Math.abs(newY / this.imageHeigth)

    if (newY > 0) {
      scale = 1 + percent
      zIndex = 10
    } else {
      blur = Math.min(20, percent * 20)
    }

    this.$refs.layer.style[transform] = `translate3d(0, ${translateY}px, 0)`
    this.$refs.filter.style[backdrop] = `blur(${blur}px)`

    if (newY < this.minTranslateY) {
      zIndex = 10
      this.$refs.bgImage.style.paddingTop = 0
      this.$refs.bgImage.style.height = `${RESERVED_HEIGHT}px`
      this.$refs.playBtn.style.display = 'none'
    } else {
      this.$refs.bgImage.style.paddingTop = '70%'
      this.$refs.bgImage.style.height = 0
      this.$refs.playBtn.style.display = ''
    }

    this.$refs.bgImage.style[transform] = `scale(${scale})`
    this.$refs.bgImage.style.zIndex = zIndex
  }
}
</script>

